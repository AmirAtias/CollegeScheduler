
public class Hour {
int hour;
int courseCode;
public Hour(int hour) {
	this.hour=hour;
	courseCode=-1;
}
public int getHour() {
	return hour;
}
public void setHour(int hour) {
	this.hour = hour;
}
public int getCourseCode() {
	return courseCode;
}
public void setCourseCode(int courseCode) {
	this.courseCode = courseCode;
}

}
